#ifndef PIN_CONFIG_H
#define PIN_CONFIG_H

/*
 * RP2040 ピン割り当て・ハードウェアリソース一覧 (Pico W)
 * ============================================================
 * 
 * GPIO  | 機能           | 方向  | 備考
 * ------|----------------|-------|----------------------------
 *  0    | (UART0 TX)     |       | ※未使用（stdio USB使用時）
 *  1    | (UART0 RX)     |       | ※未使用（stdio USB使用時）
 *  2    | DEBUG GPIO #0  | OUT   | デバッグ出力
 *  3    | DEBUG GPIO #1  | OUT   | デバッグ出力
 *  4    | DEBUG GPIO #2  | OUT   | デバッグ出力
 *  5-15 | (未使用)       |       |
 *  16   | UART0 TX       | OUT   | 外部UART送信
 *  17   | UART0 RX       | IN    | 外部UART受信
 *  18-19| (未使用)       |       |
 *  20   | SW2            | IN    | DIPスイッチ2（外部プルアップ）
 *  21   | SW1            | IN    | DIPスイッチ1（外部プルアップ）
 *  22-28| (未使用)       |       |
 *  LED  | CYW43内蔵LED   | OUT   | ステータス表示（WiFiチップ経由）
 * 
 * ペリフェラル・リソース使用状況:
 *  - UART0     : 外部デバイス通信（GPIO16/17）
 *  - DMA       : BT→UART送信用（チャネル動的割り当て）
 *  - Watchdog  : システム監視タイマー（タイムアウト10秒、閾値5回）
 *  - Timer     : SDK内部使用（time_us_64, busy_wait_us, to_ms_since_boot）
 *                BTStackソフトウェアタイマー（1msシステム処理）
 *  - CYW43     : Bluetooth SPP + LED制御
 * ============================================================
 */

// ============================================================
// UART ピン・リソース定義
// ============================================================
#define PIN_UART_TX             16          // GPIO 16 - UART0 TX
#define PIN_UART_RX             17          // GPIO 17 - UART0 RX
#define UART_PERIPH             uart0       // 使用するUARTペリフェラル

// ============================================================
// デバッグGPIO定義
// ============================================================
#define PIN_DEBUG_0             2           // GPIO 2 - デバッグ出力 #0
#define PIN_DEBUG_1             3           // GPIO 3 - デバッグ出力 #1
#define PIN_DEBUG_2             4           // GPIO 4 - デバッグ出力 #2
#define DEBUG_GPIO_COUNT        3           // デバッグGPIOピン数

// ============================================================
// スイッチ入力定義
// ============================================================
#define PIN_SW1                 21          // GPIO 21 - DIPスイッチ1
#define PIN_SW2                 20          // GPIO 20 - DIPスイッチ2

// ============================================================
// ウォッチドッグ設定
// ============================================================
#define WDT_TIMEOUT_MS          10000       // ウォッチドッグタイムアウト（10秒）
#define WDT_RESET_THRESHOLD     5           // リセット回数閾値

#endif // PIN_CONFIG_H
